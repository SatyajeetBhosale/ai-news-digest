def send_email(subject: str, html_body: str, sender: str, password: str, receiver: str):
    """Send HTML email via Gmail SMTP."""
    msg = MIMEMultipart("alternative")
    msg["Subject"] = subject
    msg["From"]    = sender
    msg["To"]      = receiver

    msg.attach(MIMEText(html_body, "html"))

    with smtplib.SMTP_SSL("smtp.gmail.com", 465) as server:
        server.login(sender, password)
        server.sendmail(sender, receiver, msg.as_string())


def main():
    load_env()

    sender   = os.getenv("EMAIL_SENDER")
    password = os.getenv("EMAIL_PASSWORD")
    receiver = os.getenv("EMAIL_RECEIVER")

    if not all([sender, password, receiver]):
        print("❌ Email credentials missing in .env file.")
        print("   Make sure EMAIL_SENDER, EMAIL_PASSWORD, EMAIL_RECEIVER are set.")
        exit(1)

    if not os.path.exists(DIGEST_FILE):
        print("❌ digest.md not found. Run summariser.py first.")
        exit(1)

    print("\n📧 AI News Emailer\n")

    # Read digest
    with open(DIGEST_FILE, "r", encoding="utf-8") as f:
        digest_md = f.read()

    # Build email
    today   = date.today().strftime("%d %B %Y")
    subject = f"🗞️ Weekly AI Digest — {today}"
    html    = build_email_html(digest_md)

    # Send
    print(f"   Sending to {receiver}...")
    try:
        send_email(subject, html, sender, password, receiver)
        print(f"✅ Email sent successfully!")
        print(f"   Subject: {subject}")
        print(f"   Check your inbox at {receiver}")
    except Exception as e:
        print(f"❌ Failed to send email: {e}")
        print("\n   Common fixes:")
        print("   - Check your Gmail App Password is correct in .env")
        print("   - Make sure 2-Step Verification is ON in your Google account")
        print("   - Check you used an App Password, not your normal Gmail password")


if __name__ == "__main__":
    main()
