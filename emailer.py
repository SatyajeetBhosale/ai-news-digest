"""
emailer.py — AI News Digest Emailer
Reads digest.md, converts to HTML email, sends to Gmail inbox.
"""

import os
import smtplib
import re
from datetime import date
from email.mime.multipart import MIMEMultipart
from email.mime.text import MIMEText

# ── CONFIG ───────────────────────────────────────────────────────────────────
BASE_DIR    = os.path.dirname(os.path.abspath(__file__))
DIGEST_FILE = os.path.join(BASE_DIR, "digest.md")
ENV_FILE    = os.path.join(BASE_DIR, ".env")
# ─────────────────────────────────────────────────────────────────────────────


def load_env():
    """Load credentials from .env file — handles UTF-16 encoding from Windows PowerShell."""
    if os.path.exists(ENV_FILE):
        raw     = open(ENV_FILE, "rb").read()
        cleaned = raw.replace(b"\xff\xfe", b"").replace(b"\xfe\xff", b"").replace(b"\x00", b"")
        for line in cleaned.decode("utf-8").strip().splitlines():
            if "=" in line and not line.startswith("#"):
                k, v = line.split("=", 1)
                os.environ[k.strip()] = v.strip()


def markdown_to_html(md: str) -> str:
    """Convert markdown digest to clean HTML email."""

    html = md

    # Headers
    html = re.sub(r"^## (.+)$",   r"<h2>\1</h2>",   html, flags=re.MULTILINE)
    html = re.sub(r"^### (.+)$",  r"<h3>\1</h3>",   html, flags=re.MULTILINE)
    html = re.sub(r"^#### (.+)$", r"<h4>\1</h4>",   html, flags=re.MULTILINE)

    # Bold
    html = re.sub(r"\*\*(.+?)\*\*", r"<strong>\1</strong>", html)

    # Italic
    html = re.sub(r"\*(.+?)\*", r"<em>\1</em>", html)

    # Links [text](url)
    html = re.sub(r"\[([^\]]+)\]\(([^)]+)\)", r'<a href="\2" style="color:#0066cc;">\1</a>', html)

    # Bullet points
    html = re.sub(r"^- (.+)$", r"<li>\1</li>", html, flags=re.MULTILINE)
    html = re.sub(r"(<li>.*</li>\n?)+", r"<ul>\g<0></ul>", html, flags=re.DOTALL)

    # Numbered lists
    html = re.sub(r"^\d+\. (.+)$", r"<li>\1</li>", html, flags=re.MULTILINE)

    # Horizontal rules
    html = html.replace("---", "<hr style='border:none;border-top:1px solid #eee;margin:20px 0;'>")

    # Line breaks
    html = html.replace("\n\n", "</p><p>")
    html = f"<p>{html}</p>"

    return html


